			const modalForm = document.querySelector('#myModal form[action="form_submit.php"]');
			if (!modalForm) return;

			modalForm.addEventListener('submit', async function(e) {
				e.preventDefault();

				const container = modalForm.closest('.form-block') || modalForm.parentElement;
				const responseEl = container ? container.querySelector('#responseMessage') : document.getElementById('responseMessage');
				const submitBtn = modalForm.querySelector('button[type="submit"]');
				const originalBtnHtml = submitBtn ? submitBtn.innerHTML : null;

				if (responseEl) responseEl.innerHTML = 'Sending...';
				if (submitBtn) {
					submitBtn.disabled = true;
					submitBtn.innerHTML = 'Sending...';
				}

				try {
					const res = await fetch(modalForm.action, {
						method: 'POST',
						body: new FormData(modalForm),
						headers: {
							'X-Requested-With': 'XMLHttpRequest'
						}
					});

					const text = await res.text();

					if (responseEl) {
						const trimmed = (text || '').trim();
						if (trimmed.length === 0) {
							responseEl.innerHTML = '<div class="alert alert-success">Thank you! Your review has been submitted successfully.</div>';
						} else {
							responseEl.innerHTML = text;
						}
					}

					// reset form on success (optional)
					modalForm.reset();
				} catch (err) {
					if (responseEl) responseEl.innerHTML = '<div class="alert alert-danger">Request failed. Try again later.</div>';
					console.error(err);
				} finally {
					if (submitBtn) {
						submitBtn.disabled = false;
						submitBtn.innerHTML = originalBtnHtml;
					}
				}
			});
		});
	</script>
